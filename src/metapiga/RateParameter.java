    public static RateParameter verboseValueOf(final String verbose) {
        RateParameter[] values;
        for (int length = (values = values()).length, i = 0; i < length; ++i) {
            final RateParameter r = values[i];
            if (r.name.equals(verbose)) {
                return r;
            }
        }
        return null;
    }
    
    public String verbose() {
        return this.name;
    }
    
    public static RateParameter[] getParametersOfModel(Parameters.EvaluationModel model) {
        switch (model) {
            case HKY85: {
                model = Parameters.EvaluationModel.K2P;
                break;
            }
            case WAG:
            case JTT:
            case DAYHOFF:
            case VT:
            case BLOSUM62:
            case CPREV:
            case MTREV:
            case RTREV:
            case MTMAM: {
                model = Parameters.EvaluationModel.GTR20;
                break;
            }
            case ECM: {
                model = Parameters.EvaluationModel.GTR64;
                break;
            }
        }
        final Set<RateParameter> set = new TreeSet<RateParameter>();
        RateParameter[] values;
        for (int length = (values = values()).length, i = 0; i < length; ++i) {
            final RateParameter p = values[i];
            if (p.model == model) {
                set.add(p);
            }
        }
        return set.toArray(new RateParameter[0]);
    }

